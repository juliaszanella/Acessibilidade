    root.style.setProperty('--cor_nav', 'white');
    root.style.setProperty('--cor_header', 'white');
    root.style.setProperty('--cor_main', 'white');
    root.style.setProperty('--cor_fonte01', 'black');
    root.style.setProperty('--cor_fonte02', 'white');
    root.style.setProperty('--cor_footer', 'white');
